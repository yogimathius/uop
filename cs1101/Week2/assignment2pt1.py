pi = 3.14159
def print_circum(radius): # Function to calculate and print the circumference of a circle, accepting the radius as an argument
  circumference = 2 * pi * radius # Calculate the circumference using the formula 2 * pi * radius
  print("The circle's circumference is: ", circumference) # Print the calculated circumference

print_circum(43)
# > The circle's circumference is:  270.17674
print_circum(25)
# > The circle's circumference is:  157.0795
print_circum(62)
# > The circle's circumference is:  389.55716

